			<p class="lang" key="c_text4"> 
				Шымкент – большой южный город, где отлично сохранилась среднеазиатская атмосфера.
				Как много времени выделить? 1-2 дня
				Главные достопримечательности: этнографический музей Кылует, Аллея памяти в парке Абая, улица Арбат
				Как сойти с проторенной дороги: осмотрите мавзолеи и продуктовый базар в селе Сайрам или покатайтесь на лошадях в заповеднике Аксу-Жабаглы.
				 
				Туркестан – столица Южного Казахстана, небольшой, но древний город и святое место паломничества.
				Как много времени выделить? 1 день
				Главные достопримечательности: комплекс Ходжа Ахмеда Ясави с подземной мечетью, музеем, мавзолеями и городской стеной
				Как сойти с проторенной дороги: присоединяйтесь к религиозным паломникам в Мавзолее Арыстан Баб или побродите по руинам Отрара.
				 
				Актау – город на дальнем западе, известный своей нефтью, протяженной береговой линией Каспийского моря и близостью к Кавказу.
				Как много времени выделить? 1 день
				Главные достопримечательности: площадь Республики, набережная и морской порт Актау, памятник Кашагану Куржиманулы
				Как сойти с проторенной дороги: спуститься в низину Карагие или полюбоваться горой Шеркала на Мангышлаке.
				 
				Байконур – неприметный город, затмеваемый близлежащим космодромом Байконур, местом советской космической программы.
				Как много времени выделить? 1-2 дня
				Главные достопримечательности: макет ракеты «Союз», музей истории космодрома, памятник Юрию Гагарину
				Как сойти с проторенной дороги: проведите день на космодроме Байконур (требуется предварительное разрешение) или совершите поездку на выходные на Кокаральскую плотину Аральского моря и деревню Каратерен.
