			<h3 class="lang" key="c_tour3">Сколько дней выделить на путешествие по Казахстану?</h3>
			<p class="lang" key="c_text3">Крупные города, такие как Нур-Султан и Алматы, принимают на себя большую часть туристического потока. Все они предлагают широкий выбор развлечений и вариантов досуга, помимо посещения исторических и современных достопримечательностей. Однако жаль, что большая часть природных локаций так легко игнорируется торопливыми туристами. Если вы хотите спланировать действительно увлекательное путешествие в Казахстан, изучите наше руководство и создайте собственный подробный маршрут вашего захватывающего приключения по стране.

			Нур-Султан (Астана) – модный и современный город Казахстана, знаменитый своей ветреной погодой и футуристической архитектурой.
			Как много времени выделить? 1-2 дня
			Главные достопримечательности: Башня Байтерек, ТЦ «Хан-Шатыр», Дворец мира и согласия.
			Как сойти с проторенной дороги: совершите однодневную поездку в национальный парк Бурабай или в Коргалжынский заповедник.
			 
			Алматы – бывшая столица и самый крупный город страны. В архитектурном облике Алматы впечатляюще сочетаются советский и современный стили.
			Как много времени выделить? 1-2 дня
			Главные достопримечательности: Парк Панфилова, Зеленый базар, Алматинская канатная дорога до Кок-Тюбе
			Как сойти с проторенной дороги: совершите путешествие во времени, побывав в этно-деревне гуннов, которые ведут быт и хозяйство так же, как их прадеды столетия назад. Любители впечатляющих видов могут отправиться к высокогорному спортивному комплексу «Медеу», недалеко от Алматы, и обозреть пейзаж с видом на каток и окрестные горные хребты.
			 
			Алматинская область – Вблизи Алматы можно найти массу локаций для однодневных поездок, которые они заслуживают особого внимания.
			Как много времени выделить? 2 дня (кемпинг по дороге или однодневные поездки из Алматы)
			Главные достопримечательности: Чарынский каньон, Большое Алматинское озеро, Тургеньское ущелье и водопад
			Как сойти с проторенной дороги: полюбуйтесь петроглифами Тамгалы или разбейте лагерь в заповеднике Алтын-Эмель.</p>
