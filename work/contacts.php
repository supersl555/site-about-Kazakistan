			<h2 class="lang" key="c_tour2">Путешествие в Казахстан в разные сезоны</h2>
			<p class="lang" key="c_text2">
			Весной и осенью в Казахстане самый умеренный климат, однако благодаря огромным размерам страны наслаждаться теплой погодой можно в разных ее уголках в разное время.

			Зима – идеальное время для катания на лыжах, сноуборде и коньках. Сделать это можно в современных горнолыжных курортах Медеу и Шымбулак в Алматы. Также зима – отличная возможность посетить музеи, парные бани и праздничные мероприятия в крупных городах Казахстана.

			Осенью в Казахстане есть чем заняться, поскольку большая часть страны озаряется яркими осенними пейзажами, а погодные условия хорошо подходят для пеших прогулок и экскурсий по городам и за их пределами.

			Весна – это сезон походов по каньонам, изучения петроглифов, туров в Южный Туркестан и Шымкент и путешествий на поезде, где вы сможете насладиться пейзажами заснеженных гор и цветущих лугов.

			Лето обычно жаркое на юге и умеренное на севере страны. Летом можно как изучать столицу, так и отправиться в национальный парк Бурабай, посетить станцию Байконур или побывать на Каспийское море недалеко от Актау.

			Ответ на вопрос «Чем заняться в Казахстане?» может быть раскрыт сотнями вариантов ответов. В стране действительно есть всё, чтобы активно, интересно и с пользой провести бесценные дни вашего путешествия.
